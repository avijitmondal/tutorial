package main

import "fmt"

func main() {

	// String values
	var a = "Avijit"
	fmt.Println(a)

	// Integer values
	var b, c int = 1, 2
	fmt.Println(b, c)

	// Boolean value
	var d = true
	fmt.Println(d)

	// Default value
	var e int
	fmt.Println(e)

	// String value
	f := "apple"
	fmt.Println(f)
}
